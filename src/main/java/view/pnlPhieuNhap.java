    private void filterTable(String query) {
        DefaultTableModel model = (DefaultTableModel) tblPhieuNhap.getModel();
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(model);
        tblPhieuNhap.setRowSorter(sorter);

        if (query.trim().isEmpty()) {
            sorter.setRowFilter(null); // Nếu ô tìm kiếm trống, hiển thị tất cả dữ liệu
        } else {
            // Lọc dữ liệu dựa trên chuỗi tìm kiếm, không phân biệt chữ hoa/thường
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
        }
    }
    
    private void loadTableData(TaiKhoan taiKhoan) {
        DefaultTableModel model = (DefaultTableModel) tblPhieuNhap.getModel();
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(model);
        tblPhieuNhap.setRowSorter(sorter);
        model.setRowCount(0); // Xóa dữ liệu cũ trên bảng

        try (Connection conn = new DatabaseConnection().getConnection()) {
            String sql = "SELECT MaPhieuNhap, NgayNhap, MaNhanVien, NhaCungCap, MaHang, SoLuongNhap FROM PhieuNhap WHERE ID_TaiKhoan = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, taiKhoan.getID_TaiKhoan());
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                String maPhieu = rs.getString("MaPhieuNhap");
                Date ngayNhap = rs.getDate("NgayNhap");
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                String ngayNhap1 = sdf.format(ngayNhap);
                String maNhanVien = rs.getString("MaNhanVien");
                String nhaCungCap = rs.getString("NhaCungCap");
                String maHang = rs.getString("MaHang");
                int soLuongNhap = rs.getInt("SoLuongNhap");
                
                Object[] row = {maPhieu, ngayNhap1, maNhanVien, nhaCungCap, maHang, soLuongNhap};
                model.addRow(row);
            }
        } catch (Exception ex) {
            Logger.getLogger(pnlNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Không thể tải dữ liệu từ cơ sở dữ liệu 3!");
        }
    }
    
