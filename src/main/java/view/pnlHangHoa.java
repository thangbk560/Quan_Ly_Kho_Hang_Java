    private void btnSuaHangHoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaHangHoaActionPerformed
        int selectedRow = tblHangHoa.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hàng hóa để sửa!");
            return;
        }

        // Lấy thông tin
        String maHH = tblHangHoa.getValueAt(selectedRow, 0).toString();
        String tenHH = tblHangHoa.getValueAt(selectedRow, 1).toString();
        int soluongTon = Integer.parseInt(tblHangHoa.getValueAt(selectedRow, 2).toString());
        String donviTinh = tblHangHoa.getValueAt(selectedRow, 3).toString();
        float donGia = Float.parseFloat(tblHangHoa.getValueAt(selectedRow, 4).toString());
        String moTa = tblHangHoa.getValueAt(selectedRow, 5).toString();
        String nhaSanXXuat = tblHangHoa.getValueAt(selectedRow, 6).toString();

        HH_Sua suaForm = new HH_Sua(taiKhoan, maHH, tenHH, soluongTon, donviTinh, donGia, moTa, nhaSanXXuat);
        suaForm.setVisible(true);
        
        suaForm.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent evt) {
                loadTableData(taiKhoan); // Cập nhật lại bảng sau khi sửa
            }
        });
    }//GEN-LAST:event_btnSuaHangHoaActionPerformed

    private void btnXoaHangHoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaHangHoaActionPerformed
        int selectedRow = tblHangHoa.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hàng hóa để xóa!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa hàng hóa này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String maHH = tblHangHoa.getValueAt(selectedRow, 0).toString();

            try (Connection conn = new DatabaseConnection().getConnection()) {
                String sql = "DELETE FROM HangHoa WHERE MaHang = ? AND ID_TaiKhoan = ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, maHH);
                ps.setInt(2, taiKhoan.getID_TaiKhoan());
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                loadTableData(taiKhoan); // Cập nhật lại bảng sau khi xóa
            } catch (Exception ex) {
                Logger.getLogger(pnlNhanVien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Xóa thất bại!");
            }
        }
    }//GEN-LAST:event_btnXoaHangHoaActionPerformed

