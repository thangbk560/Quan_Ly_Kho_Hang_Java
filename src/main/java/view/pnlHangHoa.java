    private void filterTable(String query) {
        DefaultTableModel model = (DefaultTableModel) tblHangHoa.getModel();
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(model);
        tblHangHoa.setRowSorter(sorter);

        if (query.trim().isEmpty()) {
            sorter.setRowFilter(null); // Nếu ô tìm kiếm trống, hiển thị tất cả dữ liệu
        } else {
            // Lọc dữ liệu dựa trên chuỗi tìm kiếm, không phân biệt chữ hoa/thường
            sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
        }
    }
    
    private void loadTableData(TaiKhoan taiKhoan) {
        DefaultTableModel model = (DefaultTableModel) tblHangHoa.getModel();
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(model);
        tblHangHoa.setRowSorter(sorter);
        model.setRowCount(0); // Xóa dữ liệu cũ trên bảng

        try (Connection conn = new DatabaseConnection().getConnection()) {
            String sql = "SELECT MaHang, TenHang, SoLuongTon, DonViTinh, DonGia, MoTa, NhaSanXuat FROM HangHoa WHERE ID_TaiKhoan = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, taiKhoan.getID_TaiKhoan());
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                Object[] row = {
                    rs.getString("MaHang"),
                    rs.getString("TenHang"),
                    rs.getInt("SoLuongTon"),
                    rs.getString("DonViTinh"),
                    rs.getFloat("DonGia"),
                    rs.getString("MoTa"),
                    rs.getString("NhaSanXuat")
                };
                model.addRow(row);
            }
        } catch (Exception ex) {
            Logger.getLogger(pnlHangHoa.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Không thể tải dữ liệu từ cơ sở dữ liệu 2!");
        }
    }
    
