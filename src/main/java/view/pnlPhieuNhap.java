    private void btnSuaPhieuNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaPhieuNhapActionPerformed
        int selectedRow = tblPhieuNhap.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn phiếu nhập để sửa!");
            return;
        }

        // Lấy thông tin
        String phieuNhap = tblPhieuNhap.getValueAt(selectedRow, 0).toString();
        String ngayNhap1 = tblPhieuNhap.getValueAt(selectedRow, 1).toString();
        String maNhanVien = tblPhieuNhap.getValueAt(selectedRow, 2).toString();
        String nhaCungCap= tblPhieuNhap.getValueAt(selectedRow, 3).toString();
        String maHang = tblPhieuNhap.getValueAt(selectedRow, 4).toString();
        int soluongNhap = Integer.parseInt(tblPhieuNhap.getValueAt(selectedRow, 5).toString());
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Date ngayNhap = null;
        try {
            ngayNhap = sdf.parse(ngayNhap1);
        } catch (ParseException ex) {
            Logger.getLogger(pnlPhieuNhap.class.getName()).log(Level.SEVERE, null, ex);
        }

        PN_Sua suaForm = new PN_Sua(taiKhoan, phieuNhap, ngayNhap, maNhanVien, nhaCungCap, maHang, soluongNhap);
        suaForm.setVisible(true);
        
        suaForm.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent evt) {
                loadTableData(taiKhoan); // Cập nhật lại bảng sau khi sửa
            }
        });
    }//GEN-LAST:event_btnSuaPhieuNhapActionPerformed

    private void btnXoaPhieuNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaPhieuNhapActionPerformed
        int selectedRow = tblPhieuNhap.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn phiếu nhập để xóa!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa phiếu nhập này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            String maPN = tblPhieuNhap.getValueAt(selectedRow, 0).toString();
            String maNV = tblPhieuNhap.getValueAt(selectedRow, 2).toString();
            String maHH = tblPhieuNhap.getValueAt(selectedRow, 4).toString();
            int soluongNhap = Integer.parseInt(tblPhieuNhap.getValueAt(selectedRow, 5).toString());

            try (Connection conn = new DatabaseConnection().getConnection()) {
                String sql = "DELETE FROM PhieuNhap WHERE MaPhieuNhap = ? AND MaNhanVien = ? AND MaHang = ? AND ID_TaiKhoan = ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, maPN);
                ps.setString(2, maNV);
                ps.setString(3, maHH);
                ps.setInt(4, taiKhoan.getID_TaiKhoan());
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Xóa thành công!");
                loadTableData(taiKhoan); // Cập nhật lại bảng sau khi xóa
            } catch (Exception ex) {
                Logger.getLogger(pnlNhanVien.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Xóa thất bại!");
            }
        }
    }//GEN-LAST:event_btnXoaPhieuNhapActionPerformed

